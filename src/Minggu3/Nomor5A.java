package Minggu3;

public class Nomor5A {
    public static void main(String[] args) {
        // Deklarasi array String
        String adib[] = {"Muhammad", "Sulthonul", "Adib", "Fahruddin", "Ali"};

        // Looping dan print setiap elemen array
        for (int i = 0; i < adib.length; i++) {
            System.out.print(adib[i] + " ");
        }
    }

}
